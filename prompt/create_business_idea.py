def get_sequential_messages():
    """
    指定された3つのメッセージを順番に返すジェネレーター関数。
    """
    messages = [
        "あなたに添付されているファイルを基に、技術の強みを最大限発揮する最も現実的な事業アイデアを考えてください。",
        "別の事業アイデアを考えてください。先ほどより少し発展的な内容でいいですが、現実的に提案してください。",
        "別の事業アイデアを考えてください。誰もが思い付かないような画期的なアイデアをください。ただし、机上の空論にならないように、なるべく現実的な提案をして欲しいです。"
    ]
    for message in messages:
        yield message